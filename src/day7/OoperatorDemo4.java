package day7;

public class OoperatorDemo4 {
	public static void main(String[] args) {
		//RelationalOperator
		//> < >= <= ==(equal to) !=(not equal to)
		//the result of relational operator are always either true or false.
		
		int a = 5;
		int b = 6;
		System.out.println(a>b);//false
		System.out.println(a<b);//true
		System.out.println(a>=b);//false
		System.out.println(a<=b);//true
		System.out.println(a==b);//false
		System.out.println(a!=b);//true
		//this is used in if else statement.
		
	}

}
